CREATE DATABASE CRUD;
USE CRUD;

CREATE TABLE PROFILE_ (
USERNAME VARCHAR(40) PRIMARY KEY,
PASSWORD_ VARCHAR(40),
EMAIL VARCHAR(40),
USER_CODE INT,
NAME_ VARCHAR(40),
TELEPHONE INT,
SURNAME VARCHAR(40)
);

CREATE TABLE USER_ (
USERNAME VARCHAR(40) PRIMARY KEY,
PASSWORD_ VARCHAR(40),
EMAIL VARCHAR(40),
USER_CODE INT,
NAME_ VARCHAR(40),
TELEPHONE INT,
SURNAME VARCHAR(40),
GENDER VARCHAR(40),
CARD_NUMBER INT,
FOREIGN KEY (USERNAME) REFERENCES PROFILE_ (USERNAME)
);

CREATE TABLE ADMIN_ (
USERNAME VARCHAR(40) PRIMARY KEY,
PASSWORD_ VARCHAR(40),
EMAIL VARCHAR(40),
USER_CODE INT,
NAME_ VARCHAR(40),
TELEPHONE INT,
SURNAME VARCHAR(40),
CURRENT_ACOUNT VARCHAR(40),
FOREIGN KEY (USERNAME) REFERENCES PROFILE_ (USERNAME)
);

